static void
bt_slave_sv_init ()
	{
	//Objid				my_id,parent_id;
	Objid				traf_gen_comp_attr_objid;
	Objid				traf_conf_objid[3];
	char				intarrvl_rate_string [128], packet_size_string [128];
	char				start_time_string [128];
	OmsT_Dist_Handle	start_time_dist_handle;
	int 				num_rows=0;
	int 				i;

	/**	Initializes state variables associated with		**/
	/**	this process model.								**/
	FIN (bt_slave_sv_init ());

	/*	Determine the object ID of the containing module.*/
	my_id = op_id_self ();
	pkts_sent = 0;
	
	/* obtain object ID of the parent object (node) */
	parent_id = op_topo_parent (my_id);
	
	/* get the name of the node */
	op_ima_obj_attr_get (parent_id, "Slave Address", &deviceNr);
	printf("Device name : %d\n", deviceNr);

	/* Read the traffic generation parameters.	*/
	op_ima_obj_attr_get (my_id, "Slave Traffic Parameters", &traf_gen_comp_attr_objid);
	
	num_rows = op_topo_child_count (traf_gen_comp_attr_objid, OPC_OBJTYPE_GENERIC);
	
	for (i=0; i<num_rows; i++)
		{
		/* get each row objid */
		traf_conf_objid[i] = op_topo_child (traf_gen_comp_attr_objid, OPC_OBJTYPE_GENERIC, i);
		
		/* Determine the start time for traffic generation.	*/
		op_ima_obj_attr_get (traf_conf_objid[i], "Start Time", start_time_string);
		if (strcmp (start_time_string, "Never") != 0)
			{
			start_time_dist_handle = oms_dist_load_from_string (start_time_string);
			start_time[i] = oms_dist_outcome (start_time_dist_handle);
			}
		else
			{
			start_time[i] = -1.0;
			}
	
		/* Determine the stop time for traffic generation.	*/
		op_ima_obj_attr_get (traf_conf_objid[i], "Stop Time", &stop_time[i]);
		if (stop_time[i] == -1.0)
			stop_time[i] = OPC_DBL_INFINITY;
	
		/* Determine the traffic type.	*/
		op_ima_obj_attr_get (traf_conf_objid[i], "Service Type", &serv_type[i]);
	
		/* If the start time is set to "Never", then there	*/
		/* is no need to schedule an interrupt as this node has	*/
		/* been set will not generate any traffic.		*/		
		if ((start_time[i] >= 0.0) && (stop_time[i] > start_time[i]))
			{
			op_ima_obj_attr_get (traf_conf_objid[i], "Interarrival Time", intarrvl_rate_string);
			intarrvl_time_dist_handle[i] = oms_dist_load_from_string (intarrvl_rate_string);
		
			/* Load the distribution used to determine the size of	*/
			/* the packets being generated.		*/					
			op_ima_obj_attr_get (traf_conf_objid[i], "Packet Size", packet_size_string);
			packet_size_dist_handle[i] = oms_dist_load_from_string (packet_size_string);
			}
	
		}
	
	/*	Initialize the packet generation status statistic	*/
	/*	to indicate that currently there are no packets 	*/
	/*	being generated.									*/
	pksize_stathandle = op_stat_reg ("Stochastic.Packet Generation Status", OPC_STAT_INDEX_NONE, OPC_STAT_LOCAL);
	op_stat_write (pksize_stathandle, (double) OPC_FALSE);

	
	/* Initilaize the statistic handles to keep	 */
	/* track of traffic Sourced by this process. */	
	bits_sent_stathandle 		= op_stat_reg ("Stochastic.Traffic Sent (bits)",		OPC_STAT_INDEX_NONE, OPC_STAT_LOCAL);
	bitssec_sent_stathandle 	= op_stat_reg ("Stochastic.Traffic Sent (bits/sec)",	OPC_STAT_INDEX_NONE, OPC_STAT_LOCAL);
	pkts_sent_stathandle 		= op_stat_reg ("Stochastic.Traffic Sent (packets)",		OPC_STAT_INDEX_NONE, OPC_STAT_LOCAL);
	pktssec_sent_stathandle 	= op_stat_reg ("Stochastic.Traffic Sent (packets/sec)",	OPC_STAT_INDEX_NONE, OPC_STAT_LOCAL);

	bits_sent_gstathandle 		= op_stat_reg ("Stochastic.Traffic Sent (bits)",		OPC_STAT_INDEX_NONE, OPC_STAT_GLOBAL);
	bitssec_sent_gstathandle 	= op_stat_reg ("Stochastic.Traffic Sent (bits/sec)",	OPC_STAT_INDEX_NONE, OPC_STAT_GLOBAL);
	pkts_sent_gstathandle 		= op_stat_reg ("Stochastic.Traffic Sent (packets)",		OPC_STAT_INDEX_NONE, OPC_STAT_GLOBAL);
	pktssec_sent_gstathandle 	= op_stat_reg ("Stochastic.Traffic Sent (packets/sec)",	OPC_STAT_INDEX_NONE, OPC_STAT_GLOBAL);

	acl_bitssec_sent_stathandle = op_stat_reg ("Stochastic.Data Traffic Sent (bits/sec)", OPC_STAT_INDEX_NONE, OPC_STAT_LOCAL);
	acl_pkts_sent_stathandle = op_stat_reg ("Stochastic.Data Traffic Sent (packets)", OPC_STAT_INDEX_NONE, OPC_STAT_LOCAL);
	sco_bitssec_sent_stathandle = op_stat_reg ("Stochastic.Voice Traffic Sent (bits/sec)", OPC_STAT_INDEX_NONE, OPC_STAT_LOCAL);
	sco_pkts_sent_stathandle = op_stat_reg ("Stochastic.Voice Traffic Sent (packets)", OPC_STAT_INDEX_NONE, OPC_STAT_LOCAL);
	
	
	num_conns = num_rows;
	
	
	FOUT;
	}

